# Set bspwm configuration for Emilia
set_bspwm_config() {
	bspc config border_width 0
	bspc config top_padding 56
	bspc config bottom_padding 2
	bspc config left_padding 2
	bspc config right_padding 2
	bspc config normal_border_color "#414868"
	bspc config active_border_color "#d4be98"
	bspc config focused_border_color "#d3869b"
	bspc config presel_feedback_color "#7daea3"
}

