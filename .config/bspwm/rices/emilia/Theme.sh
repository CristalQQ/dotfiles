# Set jgmenu colors for Emilia
set_jgmenu_colors() {
	sed -i "$HOME"/.config/bspwm/jgmenurc \
		-e 's/color_menu_bg = .*/color_menu_bg = #32302f/' \
		-e 's/color_norm_fg = .*/color_norm_fg = #d4be98/' \
		-e 's/color_sel_bg = .*/color_sel_bg = #222330/' \
		-e 's/color_sel_fg = .*/color_sel_fg = #d4be98/' \
		-e 's/color_sep_fg = .*/color_sep_fg = #414868/'
}

# Set Rofi launcher config
set_launcher_config() {
	sed -i "$HOME/.config/bspwm/scripts/Launcher.rasi" \
		-e '22s/\(font: \).*/\1"JetBrainsMono NF Bold 9";/' \
		-e 's/\(background: \).*/\1#32302f;/' \
		-e 's/\(background-alt: \).*/\1#282828;/' \
		-e 's/\(foreground: \).*/\1#d4be98;/' \
		-e 's/\(selected: \).*/\1#ea6962;/' \
		-e "s/rices\/[[:alnum:]\-]*/rices\/${RICETHEME}/g"

	# NetworkManager launcher
	sed -i "$HOME/.config/bspwm/scripts/NetManagerDM.rasi" \
		-e '12s/\(background: \).*/\1#32302f;/' \
		-e '13s/\(background-alt: \).*/\1#282828;/' \
		-e '14s/\(foreground: \).*/\1#d4be98;/' \
		-e '15s/\(selected: \).*/\1#ea6962;/' \
		-e '16s/\(active: \).*/\1#a9b665;/' \
		-e '17s/\(urgent: \).*/\1#ea6962;/'

	# WallSelect menu colors
	sed -i "$HOME/.config/bspwm/scripts/WallSelect.rasi" \
		-e 's/\(main-bg: \).*/\1#32302f;/' \
		-e 's/\(main-fg: \).*/\1#d4be98;/' \
		-e 's/\(select-bg: \).*/\1#ea6962;/' \
		-e 's/\(select-fg: \).*/\1#d4be98;/'
}

# Launch the bar
launch_bars() {

	for mon in $(polybar --list-monitors | cut -d":" -f1); do
		MONITOR=$mon polybar -q emi-bar -c "${rice_dir}"/config.ini &
	done

}

### ---------- Apply Configurations ---------- ###

set_bspwm_config
set_alacritty_config
set_kitty_config
set_picom_config
launch_bars
set_eww_colors
set_jgmenu_colors
set_dunst_config
set_launcher_config
